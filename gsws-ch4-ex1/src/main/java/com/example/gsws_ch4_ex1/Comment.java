package com.example.gsws_ch4_ex1;

public class Comment {
    private String author;
    private String text;

    // getter for author
    public String getAuthor() {
        return author;
    }

    // setter for author
    public void setAuthor(String author) {
        this.author = author;
    }

    // getter for text
    public String getText() {
        return text;
    }

    // setter for text
    public void setText(String text) {
        this.text = text;
    }
}
